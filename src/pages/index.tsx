function FriendsBar() {
  return null
}

function ChatRoom() {
  const session = useSession()
  const router = useRouter()
  const isClient = useIsClient()

  useEffect(() => {
    const pusher = new Pusher(env.NEXT_PUBLIC_PUSHER_KEY, {
      cluster: env.NEXT_PUBLIC_PUSHER_CLUSTER,
    })
    const channel = pusher.subscribe("hello-channel")
    const callback = (data: unkown) => {
      console.log("We recieved a push event! ", data)
    }

    channel.bind("hello-event", callback)
    return () => {
      channel.unbind("hello-event", callback)
    }
  }, [])

  if (isClient && (!session || session.status === "unauthenticated")) {
    void router.push("/signup")
  }
  return (
    <div className="flex flex-1 flex-col bg-gray-100">
      <div className="flex flex-1 flex-col">
        <ChatHistory />
      </div>
      <div className="m-2 flex h-24 bg-gray-500">
        <Textarea
          className="h-full bg-gray-50 p-4"
          style={{ resize: "none" }}
          placeholder="Write a message"
        />
      </div>
    </div>
  )
}

